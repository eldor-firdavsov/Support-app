  status IN ('active', 'inactive', 'Left', 'On hold', 'Completed', 'Finished')
);

-- 1.5-qadam: Agar dublikatlar mavjud bo'lsa ularni o'chirish (tasodifan ikki marta ishga tushirilganda paydo bo'lgan)
DELETE FROM students
WHERE id IN (
  SELECT id
  FROM (
    SELECT id, row_number() OVER (PARTITION BY full_name, group_id ORDER BY created_at ASC) as rnum
    FROM students
  ) t
  WHERE t.rnum > 1
);

